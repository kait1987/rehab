/**
 * 랜덤 메시지 선택 유틸리티
 */
export function pickRandom<T>(messages: readonly T[]): T {
  const index = Math.floor(Math.random() * messages.length);
  return messages[index];
}

/**
 * 연속 기록 메시지 가져오기
 */
export function getStreakMessage(streak: number): string | null {
  const thresholds = [30, 14, 7, 5, 3] as const;
  for (const threshold of thresholds) {
    if (streak >= threshold) {
      return STREAK_MESSAGES[threshold];
    }
  }
  return null;
}
